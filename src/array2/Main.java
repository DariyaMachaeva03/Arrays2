    public static String readFile(String fileName) throws FileNotFoundException {
        //Этот спец. объект для построения строки
        StringBuilder sb = new StringBuilder(); // Изменяемая строка
        File file = new File(fileName); // Файл, с которым можно работать

        if (file.exists()) { // Если файл существует, то идем далее
            try {
                // Буфер хранит данные, а буферредер читает из буфера
                BufferedReader in = new BufferedReader(new FileReader(file.getAbsoluteFile()));
                try { // Пытаемся построчно считать файл
                    String s; // Создаем строку, которая будет хранить следующую строку
                    while ((s = in.readLine()) != null) { // Пока есть следующая строка
                        sb.append(s); // Добавляем строку в sb
                        sb.append("\n"); // Добавляем символ переноса строки в sb
                    }
                } finally { // Выполняется в любом случае
                    //Закрыавем файл
                    in.close();
                }
            } catch (IOException e) { // Если произошла ошибка
                throw new RuntimeException(e); // Сообщает о ней
            }
        }

        //Возвращаем полученный текст с файла
        return sb.toString();
    }

    public static void writeFile(String fileName, String text) {
        //Определяем файл
        File file = new File(fileName); // Файл, с которым можно работать

        try {
            //проверяем, что если файл не существует то создаем его
            if (!file.exists()) {
                file.createNewFile();
            }

            //PrintWriter обеспечит возможности записи в файл
            PrintWriter out = new PrintWriter(file.getAbsoluteFile());

            try {
                //Записываем текст у файл
                out.print(text);
            } finally {
                //После чего мы должны закрыть файл
                //Иначе файл не запишется
                out.close();
            }
        } catch (IOException e) {
            throw new RuntimeException(e);
        }
    }

    public static void jButton3ActionPerformedHandlerForOneString(JTextField jTextField, JLabel jLabel) {
        String input = jTextField.getText(); // Получаем строку

        // Бесконечный массив. Будет содержать цифры, которые надо просуммировать
        ArrayList<Integer> toSum = new ArrayList<Integer>();

        for (int i = 0; i < input.length(); i++) { // Цикл для прохода по строке input
            char currentChar = input.charAt(i); // Извекаем следующий символ 
            String currentCharInString = String.valueOf(currentChar); // Переделать символ в строку

            int value; // Объявляем переменную, которая Пытается хранить в себе число          
            try {
                value = Integer.valueOf(currentCharInString);
            } catch (Exception e) { // Если символ -- Не цифра
                continue; // Пропуск всего, что идет далее По Циклу
            }

            if (value % 3 == 0) { // Если цифра кратно трем
                toSum.add(value); // То добавляем ее в бесконечный массив
            }
        }

        int sum = 0; // Объявление переменную, которая будет хранить сумму цифр, кратных 3
        for (Integer value : toSum) { // Проходимся по бесконечному массиву
            sum += value; // Прибавляем каждое число из массива к сумме
        }

        // Конвертируем sum в строку, а после выводим ее в jLabel
        jLabel.setText("Результат: " + Integer.toString(sum));
    }

