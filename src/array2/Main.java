    private void jButton_calculate5Part1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_calculate5Part1ActionPerformed
        jLabel_list5Part1.setText("список: ");
        int n = 4; //вводим целое число
        int[] myList = new int[2 * n]; //сумма чисел от 2 до n
        for (int i = 1; i < myList.length; i++) { //строки,длинна массива
            myList[i] = new Random().nextInt(5);
        }
        int sumLeft = 0;
        for (int i = 0; i < n; i++) {
            sumLeft += myList[i]; // сумма слева
        }
        int sumRight = 0; //сумма справа
        for (int i = myList.length - 1; i >= n; i--) {
            sumRight += myList[i];
        }
        int result = sumLeft - sumRight; //из суммы левых чисел вычитаем сумму правых чисел
        jLabel_result5Part1.setText("Результат: " + Integer.toString(result));
        String caption = jLabel_list5Part1.getText(); // надпись jLabel
        for (int i : myList) {
            caption += Integer.toString(i) + " ";
        }
        jLabel_list5Part1.setText(caption);
    }//GEN-LAST:event_jButton_calculate5Part1ActionPerformed

    private void jButton_calculate5Part2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_calculate5Part2ActionPerformed
        // width -- длина одной строки
        // height -- количество строк
        int width = Integer.parseInt(jTextField_inputWidhtMatrix5Part2.getText());
        int height = Integer.parseInt(jTextField_inputHeighttMatrix5Part2.getText());
        int[][] A = new int[height][width];
        ArrayList<int[]> result = new ArrayList<int[]>();
        A = Generate.generateArray(A, 0, 100);

        for (int[] a : A) {
            boolean haveZero = false;
            for (int i : a) {
                if (i == 0) {
                    haveZero = true;
                }

            }
            if (haveZero) {
                result.add(a);
            }
        }

        String caption = "<html>Резульат: ";
        // Если длина результата (количество подходящих строк) равно 0
        if (result.size() == 0) {
            // Пишем, что таких строк нет
            caption += "Таких строк нет";
        } else {
            // Иначе мы выводим наши строки
            caption += "<br/>"; // разрыв строки
            for (int[] a : result) {
                for (int x : a) {

                    caption += Integer.toString(x); // преобразование числа в строку
                    caption += " ";
                }
                caption += "<br/>";
            }
        }

        jLabel_result5Part2.setText(caption + "</html>"); // Закрываем тег html

        ShowOnTable.showMatrix(A, jTable_outputMatrix5Part2);
    }//GEN-LAST:event_jButton_calculate5Part2ActionPerformed

