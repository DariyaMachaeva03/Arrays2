    public static double findGCD(double a, double b) {
        // Применяем алгоритм Евклида
        while (b != 0) {
            double temp = b;
            b = a % b;
            a = temp;
        }
        return a;
    }

    // Функция для нахождения НОД трех чисел типа double
    public static double findGCD(double a, double b, double c) {
        double gcdAB = findGCD(a, b); // Находим НОД для чисел A и B
        return findGCD(gcdAB, c); // Находим НОД для полученного НОД и числа C
    }

